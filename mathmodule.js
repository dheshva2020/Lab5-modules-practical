// mathModule.js
export function add(a, b)
    { return a + b; }

export function subtract(a, b)
    { return a - b; }

export function multiply(a, b)
    { return a * b; }
export function divide(a, b)
    { return a / b; }
export function modulus(a, b)
    { return a % b; }
export function power(a, b)
    { return a ** b; }
export function squareRoot(a)
    { return Math.sqrt(a); }
export function cubeRoot(a)
    { return Math.cbrt(a); }
export function absoluteValue(a)
    { return Math.abs(a); }
export function maximum(a, b)
    { return Math.max(a, b); }
export function minimum(a, b)
    { return Math.min(a, b); }
export function round(a)
    { return Math.round(a); }
export function floor(a)
    { return Math.floor(a); }
    
